import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        int n = 1;
        Scanner scan = new Scanner(System.in);
        while(scan.hasNext()){
            String a = scan.nextLine();
            System.out.println(n + " "+a);
            n++;
        } 
    }
}
